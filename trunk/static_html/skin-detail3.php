<p>Suka sebel pas bangun tidur dan ngeliat muka kusam dan kusut? Atau pernah bangun dan ngedapetin <em>nightmare</em> di pagi hari pas ngeliat ada jerawat di muka? <em>Worry no more</em>! Ada triknya kok biar pas bangun, kita tetep cantik dan segar! Intip yuk!</p>
<p>1.&nbsp;<em>Drink 2 glasses of water before going to bed</em>. Karena kita tidur kurang lebih 8 jam semaleman yang bikin kita mungkin nggak sempet kebangun buat minum. Padahal tubuh kita tetep perlu kandungan air yang cukup lho selama tidur, biar bangunnya nggak lemes dan kuyu.</p>
<p>2. <em>Eat maximum 2 hours before you sleep</em>. Biar tubuh punya waktu yang cukup buat mengolah makanan yang masuk. Ini juga menghindari penumpukan makanan yang bikin perut buncit lho!</p>
                    <p><img src="content/SKIN101/3/wakeup2-inside.jpg" /></p>
<p>3.&nbsp;<em>Do some moves</em>. Beberapa gerakan <em>stretching</em> ringan sebelum tidur juga ngebantu otot-otot tubuh kita jadi lebih rileks dan bakal bikin tidur kita lebih nyenyak.</p>
<p>4.&nbsp;<em>Stop overthinking</em>. Secara nggak langsung, pikiran yang ada di otak kita sebelum tidur bakal kebawa semaleman tuh. Ini yang bikin tidur kita jadi kurang nyenyak dan berasa kayak ada beban pas bangunnya.</p>
<p>5.&nbsp;Bersihin muka kita pake <strong>Clean &amp; Clear Fruit Essentials Facial Cleanser Berry and Lemon</strong> yang punya kandungan vitamin C cukup tinggi buat nggak cuma ngebersihin muka kita dari kotoran dan debu yang nggak keliatan, tapi juga punya efek melembapkan muka kita selama kita tidur.</p>
                    <p><img src="content/SKIN101/3/wakeup3-inside.jpg" /></p>
<p>Jadi, masih khawatir bangun pagi dengan muka berantakan?</p>
