                    <p>Pengen punya bibir <em>pink</em> alami? Musti rajin pake <em>lip scrub</em> nih. <em>Lip scrub</em> bisa ngebantu mengangkat sel-sel kulit mati dan membuat bibir kita jadi lembut. Bikinnya juga nggak susah kok. Yuk cobain 3 resep <em>lip scrub</em> ini!</p>
                    <p style="text-align:center;"><img src="content/DIY/1/1.1.jpg" /></p>
                    <p><strong>Sugar Lip Scrub</strong><strong> </strong></p>
                    <p>Bahan:<br />
                      1 sendok teh gula putih<br />
                      1 sendok teh <em>brown sugar</em><br />
                      1 sendok teh madu<br />
                      1/2 sendok teh <em>coconut oil</em><em>/ olive oil</em></p>
                    <p>Cara membuat:<br />
                      Campur semua bahan jadi satu di dalam sebuah mangkuk bersih.<br />
                      Aduk hingga rata.<br />
                      Pindahin ke dalam wadah kecil.</p>
                      
                    <p style="text-align:center;"><img src="content/DIY/1/1.2.jpg" /></p>
                    <p><strong>Coffee Lip Scrub</strong></p>
                    <p>Bahan:<br />
                      1 sendok teh madu<br />
                      1 sendok teh <em>coconut oil</em><em>/ olive oil</em> <br />
                      2 sendok teh bubuk kopi<br />
                      <br />
                      Cara membuat:<br />
                      Campur semua bahan jadi satu di dalam sebuah mangkuk bersih.<br />
                      Aduk hingga rata.<br />
                      Pindahkan ke dalam wadah kecil.</p>
                    <p><em>Now it's ready to use</em>! Pake <em>lip scrub </em>dua sampe tiga kali seminggu. <br />
                      Caranya, ambil sedikit <em>scrub</em> lalu usapkan pada bibir perlahan. Pijat dengan gerakan memutar selama 20 detik sampe satu menit. Bilas dengan air hangat, dan jangan lupa pake <em>lip balm</em> setelahnya <em>to keep it moist</em>. </p>
