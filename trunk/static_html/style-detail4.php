<p>Familiar dengan <em>hashtag</em> #OOTD di instagram dan pengen mulai ikutan bikin foto OOTD? Yuk pelajari triknya!</p>
<p><strong>1. Pilih background yang tepat</strong><br />
  <em>Background</em> adalah elemen yang penting dalam sebuah foto. Pastiin <em>background</em> ini menunjang <em>outfit</em> kita. Hindari <em>background</em> yang terlalu berantakan, atau terlalu penuh <em>pattern</em>-nya karena bisa ngedistraksi OOTD kita. <br />
  <em>Cheat</em>: <em>Open your eyes, play with what's around</em> kayak dinding lucu, pintu rumah yang <em>colorful</em> atau tanaman menjalar juga bisa jadi <em>background</em>. <em>Or else, make your own OOTD background with paper</em>, kain atau bahkan dinding kamar kita. </p>
<p><strong>2. Tolong, sist</strong><br />
  Memakai tongsis memang praktis untuk keperluan foto sehari-hari, tapi kalo pengen dapetin foto OOTD yang bagus, <em>then ask somebody</em>. <br />
  <em>Cheat</em>: Bisa juga nih ajak temen se-<em>gank</em> buat <em>hunting</em> foto bareng dan fotoin satu sama lain!</p>
<p><strong>3. Lighting yang tepat</strong><br />
  Foto yang buram atau gelap pasti bakal berkesan kusam. Buat foto yang bagus dan <em>outdoor</em>, kita musti pinter pilih waktunya ya. Baiknya di pagi atau sore hari saat matahari udah nggak mentereng lagi. Foto di siang hari bisa bikin <em>over light</em> dan foto kita jadi nggak terlalu <em>clear</em>. Kalo mau <em>indoor</em>, <em>prepare the lighting well </em>ya.</p>
<p><strong>4. Jangan over filter</strong><br />
  Main-main pake filter yang ada di vsco dan instagram boleh kok, asal nggak berlebihan. Kalo berlebihan dan warna jadi berubah banget, esensi buat nunjukkin outfit of the day kita juga bakal ilang karena kemakan warna filternya juga. </p>
<p><strong>5. Hashtag, please!</strong><br />
  <em>Done capturing</em>? Waktunya <em>posting</em> foto kita di instagram. Supaya semakin eksis, jangan lupa sertakan <em>hashtag</em> di setiap foto kamu ya. Dengan <em>hashtag</em>, besar kemungkinan foto kamu akan dilihat sama OOTD-ers yang lain. Bahkan bisa berujung saling nge-<em>like</em> dan kenalan deh. Untuk ciri khas, kamu juga bisa mencantumkan <em>hashtag</em> yang lebih personal seperti #(namakamu)OOTD. <br />
  Monika Paramitha Hans Putri<br />
