                    <p><img src="content/SKIN101/1/habit-inside.jpg" /></p>
<p>Setiap cewek pasti punya caranya sendiri buat ngerawat kecantikan tubuhnya masing-masing. Tapi nggak jarang, tanpa sadar kita ngelakuin kebiasaan-kebiasaan yang sebenernya nggak bagus dan harus segera diilangin. <em>Here are 5 bad beauty habits we should say goodbye, as soon as possible:</em></p>
<p><strong> 1. Sleeping in makeup</strong></p>
<p><em>Taking off our makeup is the number one priority. </em>Nggak peduli seberapa capeknya kita setelah seharian beraktivitas di luar, penting banget buat hapus <em>makeup</em> yang kita pake seharian. Sisa <em>makeup</em> ini bakalan nyumbat pori-pori yang bisa nimbulin jerawat. Belum lagi ditambah debu dan kotoran yang ikut menempel, <em>the threat of acne is definitely alarming you.</em></p>
<p><strong>2. Over-exfoliating</strong></p>
<p><em>Exfoliating</em> (pengelupasan kulit, salah satunya bisa pake <em>scrub</em>) penting dilakuin buat ngilangin sel-sel kulit mati yang masih nempel di kulit wajah. Karena hasilnya yang bikin kulit jadi super lembut, kadang kita suka ngelakuinnya setiap hari. <em>Exfoliating</em> yang berlebihan bisa bikin kulit wajah kita iritasi, lho! Sebenernya ini cukup dilakuin dua sampai tiga kali seminggu dengan menggunakan <em>scrub</em>, <em>gentle</em> <em>enzyme</em> atau <em>acid</em> <em>peel</em>.</p>
<p><strong>3. Licking and biting our lips</strong></p>
<p>Bibir kering emang nggak nyaman banget, tapi membasahi bibir dengan air liur <em>is</em> <em>a</em> <em>big</em> <em>no</em>-<em>no</em>! Air liur mengandung enzim untuk mencerna makanan dan kalo bibir dibasahi dengan air liur, enzim itu juga bakalan mencerna bibir kita dan malah bikin bibir jadi pecah-pecah. Sebagai gantinya, coba pake pelembap khusus buat mencegah bibir kering dan pecah-pecah.</p>
<p><strong>4. Picking pimples</strong></p>
<p>Memencet bukan solusi singkat buat ngilangin jerawat lho, <em>girls</em>! Ini malah semakin memperparah keadaan kulit wajah kamu, karena tangan kita yang kotor bikin kulit wajah iritasi dan muncul jerawat baru. Parahnya lagi, walaupun jerawatnya hilang, bekasnya mungkin bakalan tetep keliatan untuk waktu yang lama.</p>
<p><strong>5. Using dark polish without a base coat</strong></p>
<p>Penggunaan<em> nail</em> <em>polish</em>, apalagi yang berwarna gelap, nggak jarang bikin kuku kita keliatan agak menguning setelah pemakaian. <em>It's important to use the base coat first to protect our nails from some damaging effects of nail polish. </em>Keuntungan lainnya, <em>base</em> <em>coat</em> juga bikin kuku terlihat lebih halus dan <em>nail</em> <em>polish</em> bisa bertahan lebih lama.</p>
