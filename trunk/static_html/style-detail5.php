                    <p>Mahasiswa kayak kita biasanya nggak mau ribet soal pakaian buat ke kampus. Kaos dan <em>jeans </em>selalu jadi pakaian wajib setiap harinya. Tapi nggak ada salahnya juga kok, <em>to put some efforts in your outfit for your important day at school. Here are 4 style guide for you to have a great speech or presentation in front of class:</em></p>
                    <p><strong>1. A Little Formal Touch</strong></p>
                    <p><em>Mix stripe tee with mom jeans and loafers.</em> Tambah sentuhan formal di <em>outfit</em> yang dipake lewat <em>blazer </em>dan <em>leather bag</em> buat ngilangin kesan yang terlalu kasual.</p>
                    <p style="text-align:center;"><img src="content/STYLE/3/1.jpg" /></p>
                    <p><strong>2. Go Pastel</strong></p>
                    <p><em>Pastel is the hottest tone color this Spring</em>. Paduin beberapa aksesoris berwarna pastel dengan <em>basic color outfit</em>.</p>
                    <p style="text-align:center;"><img src="content/STYLE/3/2.jpg" /></p>
                    <p><strong>3. Get Monochromatic</strong></p>
                    <p><em>Outfit</em> dengan satu <em>tone</em> warna bikin kita keliatan <em>stunning </em>banget <em>and it’s a great way for us to draw some attentions by wearing an accessories in statement color.</em></p>
                    <p style="text-align:center;"><img src="content/STYLE/3/3.jpg" /></p>
                    <p><strong>4. Sporty Luxe</strong></p>
                    <p><em>Mix sporty stuff, like jogger pants and sneakers, with white sweater and floral backpack. Put a sleeveless blazer over the sweater and necklace to get a luxe touch.</em></p>
