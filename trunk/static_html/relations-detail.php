<p>Sering ribut sama sahabat? Cari tau yuk apa masalah utamanya dan gimana cara nyeleseinnya!</p>
<p><strong>1. Don't Waste Time On Jealousy</strong></p>
<p>Teman baik kita diangkat menjadi ketua organisasi, populer, dan punya segudang prestasi, pasti bikin kita sebagai temannya bangga. Padahal dalam hati kita kadang-kadang ngerasa iri dan kesal karena dengan kepopuleran dia, kita cuma dianggap sebagai pelengkap dan nggak pernah 'terlihat'.</p>
<p><em>How to solve it</em>: Daripada mikir yang nggak-nggak dan jadi <em>insecure</em>, mending kita fokus ke apa yang kita mau dan gimana cara ngembangin bakat kita. Bukan buat ngejar kepopuleran dia, tapi lebih buat ngemaksimalin potensi kita sendiri juga kok. </p>
<p><strong>2. My Best Friend Befriend My Best Friend</strong></p>
<p>Kita ngenalin sahabat kita sejak kecil ke sahabat yang sekarang tujuannya supaya kita makin deket satu sama lain dan temenannya makin asik, tapi kok setelah dikenalin mereka jadi akrab, terus sering ngumpul dan bikin rencana sendiri tanpa ngajak kita sama sekali? </p>
<p><em>How to solve it</em>: Wajar kalo kita awalnya bete karena mungkin ngerasa temen kita "direbut". Tapi kan kita juga yang ngenalin mereka dan bisa jadi mereka punya kesamaan minat yang bikin mereka pergi berdua tanpa ngajak kita. Nanti kalo emang rasanya kita udah mulai nggak terasa kayak terlupakan banget, mending langsung ngomongin yang enak bareng mereka berdua kalo kita ngerasa nggak dianggep temen lagi. <em>Or else, find a new friend </em>dan lebarin jaringan pertemanan kita.</p>
<p><strong>3. Guts Over Fear</strong></p>
<p>Punya temen-temen seru dan sering nongkrong di tempat-tempat hits memang bikin kita kelihatan keren dan asik. Makin kesini kok mereka nongkrongnya jadi setiap hari, dan kita sampe bela-belain bolos les cuma buat ikut mereka. Mana uang simpanan untuk beli buku juga habis dipake buat jajan di tempat nongkrong. Tapi kita juga nggak berani buat nolak ajakan mereka, karena takut nggak bakal diajak seru-seruan bareng lagi.</p>
<p><em>How to solve it</em>: Nggak semua orang di satu <em>friend circle</em> yang sama punya kegemaran yang sama pula. Boleh kok sesekali ikutan. Tapi kita juga harus tau batas diri kita sendiri. Kalo udah mulai nggak sesuai sama kondisi kita dan kesannya jadi dipaksain, ya buat apa. Kan masih bisa bareng juga buat acara lain sama mereka. Karena harusnya, <em>friends understand each other</em>.</p>
<p><strong>4. They have changed</strong></p>
<p>Sahabat baik di SMA kita ternyata kuliah di kampus yang beda sama kita. Bilangnya sih bakal terus sahabatan, tapi makin lama kok dia makin sibuk dan nggak pernah bisa kalo diajak ketemuan? Padahal dia sering banget <em>update</em> di <em>social media</em> kalo lagi ngumpul sama temen-temen barunya. Kayaknya buat temen-temennya dia selalu ada waktu ya. </p>
<p><em>How to solve it</em>: <em>Move on</em>. Satu hal yang harus kita pahami adalah tidak ada satupun di dunia ini yang bisa berlangsung selamanya. Mungkin dia lagi nikmatin kesibukan barunya sama dunia barunya. <em>So why don't we do the same </em>dan nikmatin dunia baru kita? </p>
