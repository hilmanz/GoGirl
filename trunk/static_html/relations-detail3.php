                    <p style="text-align:center"><img src="content/Relations/3/friendzone-inside.jpg" /></p>
<p>Banyak cewek yang beranggapan kalo temenan sama gebetan adalah salah satu cara yang paling tepat buat <em>upgrade</em> ke status pacaran. <em>Sometimes it works</em>, tapi sayangnya situasi kayak begini jadi bikin ribet pas cowok yang kita taksir malah ngeliat kita <em>as a friend aja</em>. <em>Since friendzone is not</em><em> our destination</em><em>,</em> perlu trik khusus nih buat <em>survive</em> dari situasi dan status 'horor' kayak begini. </p>
<p><strong>1. Admit The Friend Status</strong></p>
<p>Sebenernya <em>Friendzone</em> bukan status yang jelek kok. Justru buat beberapa hal, temenan sama gebetan bisa jadi keuntungan sendiri buat kita karena nggak perlu keliatan canggung buat terus-terusan deket sama dia. Tapi kalo emang kita pengen dianggep lebih dari sekedar temen atau sahabatnya aja, <em>we should admit that we're still in that zone and try to get closer</em>. Sesimple ngajak dia pergi ke kantin bareng buat <em>lunch</em> bareng bisa jadi awal yang bagus. Semakin sering ngabisin waktu bareng dia, semakin besar kemungkinan buat nunjukin potensi kalo kita bisa dilihat lebih dari sekedar temen aja.</p>
<p><strong>2. Overcome Your Fear!</strong></p>
<p>Mau ngajak nonton bareng, malu. Kangen tapi males nge-<em>text</em> duluan. Kadang hal-hal kayak gini yang bikin kita jadi jalan di tempat cuma karena gengsi buat <em>make a move</em> duluan. Kebanyakan kasus cewek di-<em>friendzone</em> sama cowok karena si cowok emang <em>clueless</em> apakah kita emang bener-bener tertarik sama dia atau nggak. C<em>ome on girls, it's okay to ask him first if we want to spending time together.</em>Nggak ada salahnya loh buat nunjukkin seberapa <em>effort</em> yang kamu lakuin buat ngedeketin dia, <em>and stop being ruled with our fear</em>. </p>
<p><strong>3. Take A Distance For A While</strong></p>
<p>Kedengerannya agak aneh, tapi ngejaga jarak sama gebetan bisa jadi tolak ukur apakah kita punya kemungkinan buat keluar dari <em>friendzone</em>. Dengan begini, seenggaknya kita ngasih kesempatan buat dia menyadari seberapa besar pengaruh dan peran kita dalam hidup dia, <em>and also stop taking you for granted.Just let him notice that he miss you and start to see you on different way.</em> Kalo mau ekstrim sih, coba liat reaksi dia pas kita bisa ngobrol seru sama temen cowok yang lain. Kalo ekspresinya nggak gitu enak, pertanda ada <em>feeling</em> juga ke kita tuh jangan-jangan.</p>
<p><strong>4. Confess Your Feelings</strong></p>
<p>Cowok itu makhluk <em>simple</em>. Saking <em>simple</em>-nya, mereka nggak selalu bisa dengan peka nangkep kode-kode halus yang udah kita lempar dan bikin dia sadar. Kalo udah begini caranya sih ya mau nggak mau kita beraniin diri buat ungkapin perasaan kita yang sesungguhnya ke dia. <em>Tell him how hard to stay in the wall between friendship and your truly feeling, and you really can't hide it.</em> Pilih cara ngomonginnya biar dia tau, kita cuma mau nyampein perasaan dan memperjelas status, <em>instead of</em> maksa dia jadian.</p>
<p><strong>5. <em><strong>Let Him Go</strong></em></strong><em></em></p>
<p>Dari sekian banyak usaha yang kita lakuin buat keluar dari <em>friendzone, there's nothing works just because he's not attracted to you in that way</em>. Kalo udah begitu, udah saatnya kita ngerelain kalo dia mungkin ngeliat kita cuma sebagai temen aja dan nggak ada rencana buat ngejadiin kita pacarnya. Nggak perlu <em>down</em>, udah saatnya kita lepas dan ngejauhin perasaan buat jadi lebih dari sekedar teman ke cowok yang kita taksir dan mulai buat nyari gebetan baru. </p>
